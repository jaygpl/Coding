# This is the initial list of colours
colors = ['red', 'blue', 'green', 'yellow', 'purple']

# Step 2: Slice the list to get the second and third colors
sliced_colors = colors[1:3]  # Get elements at index 1 and 2 (up to 3, not included)

# Step 3: Print the sliced list
print(sliced_colors)

# Step 4: Get and print the length of the colors list
length_of_colors = len(colors)
print(length_of_colors)